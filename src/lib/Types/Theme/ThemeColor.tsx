// Gabungkan prefix (text, border, bg, dll) dengan tingkatan angka
// Menggunakan Record untuk membuat tipe secara otomatis,
// contohnya: 'text50' | 'text100' | ...
type PrefixedColorShades<Prefix extends string> = {
    [K in keyof ColorShades as `${Prefix}${K}`]: ColorShades[K];
};

// Definisikan tipe untuk satu objek warna (misalnya 'orange')
